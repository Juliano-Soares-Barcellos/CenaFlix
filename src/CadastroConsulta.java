    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cPf = TextoCPF.getText().replaceAll("[^\\d]", "");
        String tElefone = TextoTelefone.getText().replaceAll("[^\\d]", "");
        String dAta = TextoData.getText().replaceAll("[^\\d]", "");

        if (TextoNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o nome ");

        } else if (dAta.equals("")) {
        
            JOptionPane.showMessageDialog(null, "Preencha a data certa");

        
        } else if (tElefone.equals("")) {
            
            JOptionPane.showMessageDialog(null, "Preencha o telefone");
        
        } else if (cPf.equals("")) {
            
            JOptionPane.showMessageDialog(null, "CPF INVALIDO");
        } else {

            try {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                LocalDate d = LocalDate.parse(TextoData.getText(), formatter);
                LocalDate dataAtual = LocalDate.now();

                if (d.compareTo(dataAtual) > 0) {

                    String dataTexto = TextoData.getText();
                    String nome = TextoNome.getText();
                    String cpf = TextoCPF.getText();
                    String telefone = TextoTelefone.getText();
                    String botao = "";

                    if (Bpaciente.isSelected()) {
                        botao = "sim";
                    } else {
                        botao = "nao";
                    }

                    Paciente pessoa = new Paciente(nome, cpf, telefone, dataTexto, botao);

                    telaInicial.adicionarPessoa(pessoa);

                    JOptionPane.showMessageDialog(this, "Pessoa adicionada com sucesso!");

                    this.dispose();
                    telaInicial.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Data invalida,impossivel cadastrar uma consulta com uma data passada antes do dia de hoje");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Data invalida verifique o  mes e o dia");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

